<h1>Music Opportunities</h1>
<h2>Singing</h2>
<p>Our Choir rehearses every Thursday at 7:30 pm in the Choir Room. Many sing all year, but some sing seasonally. All are most welcome to participate. The Choir does take a break during the summer months. If you have questions, please contact our Music Director, Laurie Birch (<a href="mailto:BirchLaurie@sbcglobal.net">BirchLaurie@sbcglobal.net</a>).</p>
