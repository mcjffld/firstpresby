<h1>Upcoming Events</h1>

<h2>Hoe-Down Showdown &amp; Chili Cook-Off<br />
  September 28, 4:30 p.m. - 8:30 p.m.</h2>
<p> First Presbyterian Church of Fairfield is pleased to announce its First Annual Hoe-Down and Chili Cook-Off, to be held Saturday, September 28, 2013.  Doors open at 4:30 and features live music, dancing, dinner, and a chili competition.  </p>
<p>This event is open to the public and offers an entertaining evening for the entire family.  Festivities include: Traditional Barn Dancing (square and line dancing) with a live caller and band featuring Patricia Campbell and The Reel Thing (photo below), chili cook-off competition, barbeque pork dinner, dessert and coffee.  </p>
<p><img src="../images/thereelthing.jpg" width="420" height="278" /><br />
<span style="font-size:x-small">The Reel Thing</span></p>
<p>Schedule is as follows:
  <br />
  4:30-6:15 Chili tasting
  <br />
  5:30-6:15 Band & Dancing
  <br />
  6:15-6:45 Dinner
  <br />
  6:45-7:30 Band & Dancing
  <br />
  7:30-8:30 Dessert  </p>
<p>There is a suggested donation of $10/person, $15/couple or $20/family.  
  
  Plenty of parking is available.  For more information, contact the Church at 203-374-6176.</p>
