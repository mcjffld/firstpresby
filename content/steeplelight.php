<h1>Steeple Light Newsletters</h1>
<p>Our newsletter, The Steeple Light, is produced monthly and contains news, programs and upcoming activities at First Presby.  The deadline for submitting articles is the 13th of each month and can be sent to <a href="mailto:First-Presbyterian@sbcglobal.net">First-Presbyterian@sbcglobal.net</a>.  All members are invited to submit articles and suggestions are always welcome. In order to open the Steeplelight, you need to have Adobe Reader on your computer. You can download Adobe for free by clicking on this link. [ <a href="http://get.adobe.com/reader/" target="_blank">Download Adobe Reader</a> ]</p>
