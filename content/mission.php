<h1>Mission Statement</h1>
<p> The Mission of the First Presbyterian Church of Fairfield is to lead people to a deeper commitment of faith in God and fellowship with one another, through Jesus Christ. We seek the empowerment of the Holy Spirit to broaden the use of our God-given talents, time and resources to advance the mission outreach of our congregation and the works of the larger Church.</p>
